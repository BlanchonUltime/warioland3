	ret

.asm_9a646
	ld hl, wCollectionPage
	ld a, [wJoypadDown]
	ld b, a
	bit A_BUTTON_F, b
	jr z, .no_a_btn
	ld a, [wCollectionScrollMode]
	and a
	call z, .SetScrollMode
	ld hl, wCollectionPage
	bit D_RIGHT_F, b
	jr nz, .a_right
	bit D_LEFT_F, b
	jr nz, .a_left
	ret

