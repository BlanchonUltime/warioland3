_InitTreasureCollection: ; 9a3cf (26:63cf)
	call DisableLCD
	call FillBGMap0_With7f
	call ClearVirtualOAM

	play_music2 MUSIC_TREASURE_COLLECTION

	xor a
	ldh [rSCX], a
	ld [wSCX], a
	ldh [rSCY], a
	ld [wSCY], a

	ld a, $02
	ldh [rSVBK], a
	call VBlank_9a564
	call Func_9a54e
	call Func_9a559
	xor a
	ld [wHDMA], a

	; if collection is already open,
	; then we don't need to search for
	; the right page
	ld a, [wIsCollectionOpen]
	and a
	jr nz, .skip_jump_page

	lb bc, 0, 0
	ld a, [wTransitionParam]
	and a
	jr z, .got_page
	cp NUM_TREASURES + 1
	jr nc, .got_page
.loop_find_page
	cp NUM_COLLECTION_CELLS + 1
	jr c, .found_page
	inc b
	sub NUM_COLLECTION_CELLS
	jr .loop_find_page
.found_page
	dec a
	ld c, a
.got_page
	ld a, b
	ld [wCollectionPage], a
	ld a, c
	ld [wCollectionCell], a
	ld a, TRUE
	ld [wIsCollectionOpen], a

.skip_jump_page
	call GetCurrentCollectionCellRowAndColumn
	call GetCollectionCellCoords

	ld hl, Pals_9ad45
	call LoadPalsToTempPals1
	ld hl, Pals_9ad05
	call LoadPalsToTempPals2

	decompress TreasureCollectionCursorGfx, v0Tiles0
	decompress TreasureCollectionGfx, v0Tiles2

	call LoadCollectionPageTreasureGfx

	ld a, BANK("VRAM1")
	ldh [rVBK], a
	ld hl, NumbersGfx
	ld de, v1Tiles1 tile $20
	ld bc, $14 tiles
	call CopyHLToDE_BC
	xor a
	ldh [rVBK], a

	decompress BGMap_9a2ac, wAttrmap
	decompress BGMap_9a129, wTilemap

	call ApplyPageTreasuresPals

	ld hl, wAttrmap + $22
	ld a, $09
	ld [hli], a
	ld [hli], a
	ld [hl], a
	ld hl, wAttrmap + $42
	ld [hli], a
	ld [hli], a
	ld [hl], a

	ld a, [wCollectionPage]
	inc a
	add $20
	ld [wTilemap + $30], a

	ld hl, wTilemap
	ld de, v0BGMap1
	ld bc, 18 * BG_MAP_WIDTH
	call CopyHLToDE_BC
	ld a, BANK("VRAM1")
	ldh [rVBK], a
	ld hl, wAttrmap
	ld de, v0BGMap1
	ld bc, 18 * BG_MAP_WIDTH
	call CopyHLToDE_BC

	xor a
	ldh [rVBK], a
	hlbgcoord 2, 1, v0BGMap1
	farcall PrintNumCollectedTreasures

	ld hl, wSceneObj1State
	ld a, $01
	call SetSceneObjState
	ld b, $10
	ld a, $08
	ld hl, wSceneObj9XCoord
	ld [hld], a
	ld [hl], b
	ld hl, wSceneObj10XCoord
	ld a, $98
	ld [hld], a
	ld [hl], b
	ld a, LCDCF_ON | LCDCF_BG9C00 | LCDCF_OBJ16 | LCDCF_OBJON | LCDCF_BGON
	ldh [rLCDC], a
	ld hl, wSubState
	inc [hl]
	ret
