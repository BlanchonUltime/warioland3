	jr nz, .loop
	inc a
	ld [w2dfff], a
.loop_treasures
	ld a, [w2dfff]
	call IsTreasureCollected
	jr z, .not_collected
	jr .collected

.next_treasure
	ld hl, w2dffe
	inc [hl]
	ld a, [hli]
	inc [hl] ; w2dfff
	cp NUM_COLLECTION_CELLS
	jr nz, .loop_treasures
	ret

