	and a
	jr z, .asm_9a68d
	dec [hl]
	jr .asm_9a68f
.asm_9a68d
	ld [hl], $04
.asm_9a68f
	play_sfx SFX_0EB
	stop_music2
	ld a, $0c
	ld [wSubState], a
	ret

