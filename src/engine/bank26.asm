	call .GetAttrmapAddress
	push de
	ld a, $fc
	ld [de], a
	inc a
	ld [hli], a
	inc a
	inc e
	ld [de], a
	inc a
	ld [hl], a
	pop hl
	dec h
	dec h
	dec h
	ld a, $04
	ld [hli], a
	ld [hl], a
	ld de, BG_MAP_WIDTH
	add hl, de
	ld [hld], a
	ld [hl], a
	jr .next_treasure

.collected
	ld a, [w2dfff]
	ld e, a
	ld d, $00
	ld hl, TreasureOBPals
	add hl, de
	ld a, [hl]
	push af
	call .GetAttrmapAddress
	pop af
	dec h
	dec h
	dec h
	ld [hli], a
	ld [hl], a
	dec d
	dec d
	dec d
	ld [de], a
	inc de
	ld [de], a
	jr .next_treasure

